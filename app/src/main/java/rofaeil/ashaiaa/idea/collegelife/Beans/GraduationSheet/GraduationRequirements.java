public class GraduationRequirements {
    private String Name;
    private String EarnedHours;
    private String RequiredHours;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        this.Name = name;
    }

    public String getEarnedHours() {
        return EarnedHours;
    }

    public void setEarnedHours(String earnedHours) {
        this.EarnedHours = earnedHours;
    }

    public String getRequiredHours() {
        return RequiredHours;
    }

    public void setRequiredHours(String requiredHours) {
        this.RequiredHours = requiredHours;
    }
}
